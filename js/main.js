function leave(){
	
	var sex = $('input[name="sex"]:checked').val();
    var name = $('#name').val();
    var iphone = $('#iphone').val();
    var message = $('#message').val();
    var service = $("#service").val();//不同店铺 service不同

    if(!iphone || !message) {
        alert("必填信息不能为空！");
        return;
    }
	
	$.ajax({
        url: "http://114.215.179.175:9090/leaveMessage/save",
        type: "POST",
        dataType: "jsonp",
        data: {
            source:1,
            service:service,
            sex: sex,
            name: name,
            touch:iphone,
            content:message
        },
        jsonpCallback: "__save",
        success: function (data) {
            alert("发送成功！");
            $('#name').val("");
            $('#iphone').val("");
            $('#message').val("");
        },
        error :function(data){
            console.log(data);
        }
    });
}




